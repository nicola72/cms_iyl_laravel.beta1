    <strong>Was sind Cookies?</strong>
    <br><br>
    Cookies sind Textdateien, die kleine Textschleifen enthalten, welche von einem Web Server zu einem Client (normalerweise einem Browser) und anschließend vom Client zum Server (unverändert) wieder zurückgeschickt werden. Dies erfolgt jedesmal wenn der Client auf denselben Bereich derselben Web-Domäne zugreift.
    Cookies enthalten keine Informationen, die einen Nutzer unmittelbar identifizieren können. Dennoch können die auf unsere Webseite gespeicherten persönlichen Informationen mit jenen, die in den Cookies gespeichert sind, verknüpft werden.
