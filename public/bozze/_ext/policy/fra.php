<strong>Combien de types de Cookies utilisons-nous ?</strong>
<br><br>
Sur notre site web on utilise trois types de cookies :
<ul>
<li>Cookies de session : Il s’agit de fichiers temporaires sauvegardés juste pour la durée de la session de navigation. A la fin de la session ils sont donc éliminés par le navigateur.</li>

<li>Cookies persistants : ce type de fichier reste actif après la session de navigation (par ex. si vous accédez a un site web, ce dernier est capable de vous reconnaître lors de l’accès successif). Les Cookies persistants permettent d’envoyer des messages de bienvenue, sauvegarder d’objets dans le chariot en cas d’achats en ligne ou des paramètres tels que le choix de la langue et des couleurs favories.  </li>

<li>Cookies des tiers : au cours de la navigation, l’utilisateur pourrait transmettre des Cookies a  des tiers. Si vous accédez a une page web avec des contenus incorporés comme, par exemple, ceux du site Couture, des Cookies pourraient être transmis par et envers ce site sans aucun contrôle de notre part. Pour cette raison, veuillez consulter les sites web concernés pour plus d’informations sur  les cookies utilisés et leur gestion. </li>
</ul>
