<strong>Que sont les Cookies ?</strong>
<br><br>
Les cookies sont des fichiers contenants des chaînes textuelles de dimensions petites invoyées par un serveur a un client (généralement un navigateur) et vice-versa (sans subir aucune modification) toutes les fois que le client accède a  la même part du même domaine.

Les Cookies ne contiennent aucune information capable d’identifier directement un utilisateur ; cependant, nous pouvons associer les données personnelles sauvegardées dans notre site aux informations stockées et obtenues pas les cookies.
